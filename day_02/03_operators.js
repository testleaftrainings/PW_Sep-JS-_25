let value = 10;
console.log(value++); //value=value+1 //10 -> prints the value and then increase the value by 1
console.log(value);

//Pre increment
console.log(++value); //increase the value by 1 and then print it
console.log(value);    //12

//Post decrement
console.log(value--); //print and decrease by 1
console.log(value);     //11

//Pre decrement
console.log(--value); //decrease by 1 and then print
console.log(value);














