// var
var company = "Testleaf";
console.log(company);

var company = "Qeagle";
console.log(company);

var company = 123;
console.log(company);
/**
 * var allows redeclaration of variables
 * var allows reassignment of variables
 */

// let
let browserName = "chrome";
console.log(browserName);

browserName = "msedge";
console.log(browserName);

browserName = 12345;
console.log(browserName);
/**
 * let allows reassignment of variables
 * let will not allow redeclaration of variables
 */

//const
const password = 'crmsfa';
console.log(password);

password = 'testing123';
console.log(password);
/**
 * const - redeclaration and reassignment are not allowed
 */

